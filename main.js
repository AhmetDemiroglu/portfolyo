document.addEventListener('DOMContentLoaded', function() {
  const toggleSwitch = document.querySelector('#checkbox');
  const currentTheme = localStorage.getItem('theme') || 'light';
  
  // Sayfa yüklendiğinde tema kontrolü
  document.documentElement.setAttribute('data-theme', currentTheme);
  if (currentTheme === 'dark') {
    toggleSwitch.checked = true;
    // Dark mode görselleri
    document.querySelector('#pLeft').src = './assets/image/5c81317bb78f130f5aaa4789_pen-1-1@3x-dark.png';
    document.querySelector('#pRight').src = './assets/image/5c8057df4f71c1e19f5e15a6_pen-1-2@3x-dark.png';
  }
  
  // İlk yüklemede tema renklerini uygula
  document.body.style.background = getComputedStyle(document.documentElement)
    .getPropertyValue('--background-gradient');

  // Tema değiştirme fonksiyonu
  function switchTheme(e) {
    if (e.target.checked) {
      document.documentElement.setAttribute('data-theme', 'dark');
      localStorage.setItem('theme', 'dark');
      // Dark mode görselleri
      document.querySelector('#pLeft').src = './assets/image/5c81317bb78f130f5aaa4789_pen-1-1@3x-dark.png';
      document.querySelector('#pRight').src = './assets/image/5c8057df4f71c1e19f5e15a6_pen-1-2@3x-dark.png';
    } else {
      document.documentElement.setAttribute('data-theme', 'light');
      localStorage.setItem('theme', 'light');
      // Light mode görselleri
      document.querySelector('#pLeft').src = './assets/image/5c81317bb78f130f5aaa4789_pen-1-1@3x.png';
      document.querySelector('#pRight').src = './assets/image/5c8057df4f71c1e19f5e15a6_pen-1-2@3x.png';
    }
    
    // Sayfayı yeniden yüklemeden tema değişikliğini uygula
    document.body.style.transition = 'background 0.3s ease';
    document.body.style.background = getComputedStyle(document.documentElement)
      .getPropertyValue('--background-gradient');
  }

  // Event listener'ı ekle
  toggleSwitch.addEventListener('change', switchTheme);
});

