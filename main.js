function changeLanguage(lang) {
  currentLanguage = lang;
  localStorage.setItem('language', lang);

  // Navbar elemanlarını güncelle
  document.querySelector('a[href="#hero"]').textContent = translations[lang].home;
  document.querySelector('a[href="#hakkimda"]').textContent = translations[lang].about;
  document.querySelector('a[href="#yetenekler"]').textContent = translations[lang].skills;
  document.querySelector('a[href="#portfolyo"]').textContent = translations[lang].projects;
  document.querySelector('a[href="#iletisim"]').textContent = translations[lang].contact;
  document.querySelector('.dropdown-toggle[href="#"]').textContent = translations[lang].socialMedia;

  // Hero bölümünü güncelle
  document.querySelector('.isimsoyisim h1 .ben').textContent = lang === 'tr' ? 'Ben,' : "I'm";
  document.querySelector('.lead').textContent = translations[lang].heroSubtitle;
  document.querySelector('.iletisimbutonu').textContent = translations[lang].contactButton;

  // Başlıkları güncelle
  document.querySelector('#portfolyom').textContent = translations[lang].aboutMe;
  document.querySelector('#yetenekler .baslik h2').textContent = translations[lang].skills;
  document.querySelector('#portfolyo .baslik h2').textContent = translations[lang].projects;
  document.querySelector('#iletisim .baslik h2').textContent = translations[lang].contact;

  // Dil seçici butonunu güncelle
  const langBtn = document.querySelector('.btn.btn-link.nav-link.dropdown-toggle');
  const flagCode = lang === 'tr' ? 'tr' : 'gb';
  const langText = lang === 'tr' ? 'TR' : 'EN';
  langBtn.innerHTML = `<img src="https://flagcdn.com/16x12/${flagCode}.png" alt="${langText}" class="flag-icon"> ${langText}`;

  // data-translate özelliği olan elementleri güncelle
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[lang][key]) {
      element.textContent = translations[lang][key];
    }
  });

  // Form placeholder ve value'larını güncelle
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    if (translations[lang][key]) {
      element.placeholder = translations[lang][key];
    }
  });

  document.querySelectorAll('[data-translate-value]').forEach(element => {
    const key = element.getAttribute('data-translate-value');
    if (translations[lang][key]) {
      element.value = translations[lang][key];
    }
  });

  // Özel elementleri güncelle
  updateAboutText();
  updateSkillDescriptions();

  // Footer linklerini güncelle
  const footerLinks = document.querySelectorAll('.footer-links .nav-link');
  footerLinks.forEach(link => {
    if (link.getAttribute('href') === '#top') link.textContent = translations[lang].footerLinks.home;
    if (link.getAttribute('href') === '#hakkimda') link.textContent = translations[lang].footerLinks.about;
    if (link.getAttribute('href') === '#yetenekler') link.textContent = translations[lang].footerLinks.skills;
    if (link.getAttribute('href') === '#portfolyo') link.textContent = translations[lang].footerLinks.projects;
  });

  // Ses özelliklerinin başlıklarını güncelle
  document.querySelector('#dinle').title = translations[lang].startSpeech;
  document.querySelector('#bitir').title = translations[lang].stopSpeech;
  document.querySelector('#oku').title = translations[lang].readText;

  // Ses tanıma ayarları
  let webSpeech = new webkitSpeechRecognition();
  webSpeech.lang = "tr-TR";
  webSpeech.continuous = true;

  dinle.onclick = () => webSpeech.start();
  bitir.onclick = () => webSpeech.stop();

  webSpeech.onresult = (event) => {
    let liste = event.results;
    subject.value += liste[liste.length - 1][0].transcript;
    console.log(liste);
  }

  // Ses sentezi ayarları
  oku.addEventListener("click", () => {
    let text = subject.value;
    let uttr = new SpeechSynthesisUtterance(text);
    uttr.lang = "tr-TR";
    speechSynthesis.speak(uttr);
  });
}

